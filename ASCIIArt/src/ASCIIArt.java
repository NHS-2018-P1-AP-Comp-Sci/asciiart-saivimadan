
public class ASCIIArt {
	public static void main (String[] args) {
		System.out.println("Here's a sad jack o lantern: \n");
		jackOLanternTop();
		sadJackOLantern();
		jackOLanternBottom();
		System.out.println("Here's a happy jack o lantern: \n");
		jackOLanternTop();
		happyJackOLantern();
		jackOLanternBottom();
		System.out.println("Here's another sad jack o lantern: \n");
		jackOLanternTop();
		sadJackOLantern();
		jackOLanternBottom();
		System.out.println("Here's another happy jack o lantern: \n");
		jackOLanternTop();
		happyJackOLantern();
		jackOLanternBottom();
		System.out.println("Halloween is almost here!!!");
		
	}
	public static void jackOLanternTop() {
		System.out.println("    ___|\"\"\"|___");

	
	}
	public static void jackOLanternBottom() {
		System.out.println("   \\___________/");
		System.out.println("");
	}
	public static void sadJackOLantern() {
		System.out.println("  /             \\");
		System.out.println(" /   /\\ ___ /\\   \\" );
		System.out.println("|       \\_/       |");
		System.out.println("|                 |");
		System.out.println(" \\      /\\/\\     /");
		System.out.println("  \\   /\\/\\/\\/\\  /");
	}
	public static void happyJackOLantern() {
		System.out.println("  /  /\\     /\\  \\");
		System.out.println(" /  /__\\ _ /__\\  \\" );
		System.out.println("|       /__\\      |");
		System.out.println("|                 |");
		System.out.println(" \\   \\ /\\_/\\ /   /");
		System.out.println("  \\   \\/\\_/\\/   /");
	}
}
	
